import java.util.*;
class IfElse3{
	public static void main(String[]args){
	
		Scanner obj=new Scanner(System.in);

			int num=obj.nextInt();

			if(num>10){
			System.out.println(num+"is greater than 10");
			
			}else if(num<10){
			
			
			System.out.println(num+"is smaller than 10");
			}else{
			
			System.out.println(num+"is equal to 10");

			}
	
	
	
	}



}
