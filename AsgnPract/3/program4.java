class IfElse4{

	public static void main(String[]args){

		char ch='G';


		if(ch>='a' && ch<='z'){
		
			System.out.println(ch+"is a lowercase Character");
		} else if( ch>=65 && ch<= 90){
		
			System.out.println(ch+"is a Uppercase Character");
		
		} else{
		
			System.out.println(ch+"is a not a Character");
		}


}


}
